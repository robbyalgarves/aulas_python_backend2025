import math # BIBLIOTECA ENORME DE FUNÇÕES MATEMATICA

def dobro(numero):
    return int(numero) * 2

def triplo(num):
    return int(num) * 3

def quadrado(num):
    return int(num) ** 2

def raiz_quadrada(num): # sqrt = SQUARE ROOT (RAIZ QUADRADA)
    return math.sqrt(int(num))     

